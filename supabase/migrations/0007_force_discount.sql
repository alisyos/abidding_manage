-- 0007: 표준 할인 임계값 예외 (force_discount)
--   - 표준 할인은 공시가 합계 ≥ 100,000원 시 할인가 자동 적용.
--   - 예외적으로 임계값 미달이어도 할인가를 강제 적용해야 하는 경우가 있어
--     견적별 boolean 토글을 추가.
--   - true 면 임계값과 무관하게 할인가(unit_price) 적용.
-- ════════════════════════════════════════════════════════════════════

alter table quotes
  add column if not exists force_discount boolean not null default false;
